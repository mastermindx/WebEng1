        <h2>Zusammenfassung</h2>
		<p/>
		<h3>Von der Homepage zur professionellen Website</h3>

		<p style="text-align:justify">Lernen Sie die Herausforderungen bei Planung, Entwicklung, Test und Wartung von Websites kennen.<br/> 
			Sie erwerben die Kompetenz, W3C-konforme, sichere und performante Webpräsenzen zu entwickeln. „Web-Engineering“ bietet Konzepte, Methoden, Techniken und Werkzeuge für die systematische Entwicklung von Websites und Web-Applikationen. </p>

		<p style="text-align:justify">Sie üben online den Umgang mit der Infrastruktur und den Werkzeugen, die für die Entwicklung und Wartung einer mit Twitter Bootstrap aufgebauten Website benötigt werden.<br/>
			Sie lernen insbesondere die dafür notwendigen technischen und architektonischen Grundlagen. Der Kurs führt auch in die Sicherheitsproblematik von Websystemen ein.<br/>
			Sie analysieren Angriffsmethoden und entwickeln Gegenmaßnahmen.</p>

		<p>Als Lerngegenstand für das Erkunden und Anwenden der Kursinhalte dient das mobile webbasierte Audience-Response-System <a href='https://arsnova.eu/blog/' onclick="window.open(this.href);return false;">ARSnova</a>. Es wird als Feedback-App in diesem MOOC eingesetzt. </p>

		<h3>Kursstruktur</h3>

		<p>Der Kurs ist in zwei aufeinanderfolgenden Teilen organisiert: Dieser Kurs, Web-Engineering I, beginnt am 15. Oktober 2013. Der Kurs umfasst 10 Kapitel mit jeweils 6 bis 8 Lerneinheiten und dauert 9 Wochen mit einem wöchentlichen Zeitaufwand von ca. 6 Stunden. Für Studierende an Hochschulen entspricht der Arbeitsaufwand für den gesamten Kurs 6 ECTS-Leistungspunkten. Im <a href='https://www.iversity.org/courses/web-engineering-ii-entwicklung-mobiler-html5-apps' onclick="window.open(this.href);return false;">zweiten Teil des Kurses</a> geht es ab dem 17. Januar 2014 zur eigenen HTML5-App.</p>

		<p><em>Bei ausreichenden Vorkenntnissen, die durch die Online-Abschlussprüfung des ersten Kursteils und durch Aufbau einer Website im Sinne des ersten Kursteils zu belegen sind, können Sie den Kurs auch mit dem zweiten Teil beginnen.</em></p>

		<h3>Lernziel</h3>

		<p>Am Ende des Kurses &quot;Web-Engineering I: Grundlagen der Web-Entwicklung&quot; sollen Sie die folgende Kompetenz erworben haben:</p>

		<p>Wie entwickle ich im Kundenauftrag eine standardkonforme, barrierefreie, performante und sichere Website?</p>

		<h3>An wen richtet sich dieser Kurs?</h3>

		<p>Der zweiteilige MOOC „Web-Engineering“ richtet sich an Studierende der Informatik, auch Medien- und Wirtschaftsinformatik, an Auszubildende der Fachinformatik mit der Fachrichtung Anwendungsentwicklung und an berufstätige Web-Designer/innen. Für Studierende der TH Mittelhessen im Fachbereich MNI ist der MOOC „Web-Engineering“ äquivalent zum Bachelor-Modul „Internetbasierte Systeme“ (CS 1024).</p>

		<h3>Kurszertifikate</h3>

		<p>Sie erhalten für jeden Kursteil ein Zertifikat der TH Mittelhessen, wenn Sie die folgenden Leistungen erbracht haben:</p>

		<ul>
			<li>Web-Engineering I: Bestehen der Online-Abschlussprüfung des ersten Kursteils und Erstellen einer Website</li>
			<li>Web-Engineering II: Bestehen der Online-Abschlussprüfung des zweiten Kursteils und Erstellen einer HTML5-App</li>
		</ul>

		<h3>Voraussetzung für die Kursteilnahme</h3>

		<p>Sie sollten grundlegende Kenntnisse in der Programmierung mitbringen. Für die Entwicklungswerkzeuge ist ein leistungsfähiger Laptop- oder Desktop-Rechner erforderlich, auf dem Sie Administratorrechte haben.</p>

		<h3>Unsere Kompetenzen</h3>

		<p>Wer einen Online-Kurs wie „Web-Engineering“ anbietet, sollte entsprechende Referenzen aufweisen können. In den letzten Jahren haben wir in den Bereichen E-Learning und Suchmaschinen mehrere webbasierte Systeme, wie z. B. die universitäre Suchmaschine Pharus, die Lern- und Kollaborations-Plattform eCollab oder das mobile Abstimmungssystem ARSnova, als Open-Source-Projekte entwickelt, stets als studentische Projekte beginnend und in Bachelor- und Masterprojekten zur Marktreife gebracht.</p>

		<p>Rückblickend war es der Kurs „Web-Engineering“, der die Studierenden für diese Projekte ausreichend vorbereitet hat. Wenn Sie die beiden Kursteile Web-Engineering I und II absolvieren, werden Sie Gleiches leisten können.</p>
		
