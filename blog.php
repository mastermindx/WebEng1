			Sinn und Zweck dieser Website ist es hauptsächlich die Aufgabe der praktischen Abschlussarbeit des MOOCs Web-Engineering I zu erfüllen.
			Dies bedeutet das eine Website mit Bootstrap3 und unter bestimmter Voraussetzungen erstellt und veröffentlich wird. Hier wäre zum einen die Lösungen zu
			den Aufgabe aus Kapitel 6 und 7 zu nennen, welche erheblicher Bestandteil der Abschlussarbeit sind und u.a. das Thema der "Barrierfreien Website" umfasst.
			<br/>
			Zum einen wird auf die Original-Lösung der Aufgabe referenziert (externer Link), diese wurde während des MOOCs bewertet und zum anderen wird die Lösung hier im Blog nochmals
			dargestellt. Im Bereich <a href="kapitel.html"><b>Kapitel-Übersicht</b></a> sind beide Varianten verlinkt.
			<br/>
			Gleich vorweg: dieses Website wurde mit den <a href="http://validator.w3.org/" onclick="window.open(this.href);return false;">W3C Markup Validation Service</a>, 
			den <a href="http://jigsaw.w3.org/css-validator/" onclick="window.open(this.href);return false;">W3C CSS Validation Service</a> und dem 
			<a href="http://wave.webaim.org" onclick="window.open(this.href);return false;">WAVE - Web accessibilty evaluation tool</a> getestet, wird aber ggf. nicht auf allen Seiten
			der Barrierefreiheit zu 100% gerecht (mit Ausnahme der Aufgaben aus Kapitel 6 und 7). So werden hier externe Links mittels "onclick="window.open(this.href)" in einem
			neuen Reiter des Browsers angezeigt ("target="_blank" sollte nicht mehr genutzt werden), obwohl nicht alle Browser bzw. deren PlugIns für Blinde / Sehbehinderte Nutzer dies so unterstützen.
			<br/>
			Dies ist einfach dem Komfort und der Tatsache geschuldet, dass ich persönlich es störend finde wenn links im gleichen Reiter geöffnet werden. Den Hinweis auf die
			mittlere Maustaste halte ich für unsinnig.
			<br/>
			Weiterhin verwendet diese Online-Präzens Bootstrap 3 und damit auch Funktionalitäten welche nur mittels CSS3 realisiert werden können. Die Aufgaben aus den jeweiligen
			Kapiteln waren jedoch (im nachhinhein gesehen) mittels CSS 2 bzw. 2.1 zu entwickeln. Daher gibt es in diesem Blog die Aufgaben quasi in 2 Variant:
			<br/>
			1. Die Originallösung in der Cloud mit CSS3 und 2. die abgeänderte im Blog mit CSS 2.1.
			<br/><br/>
			Weiterhin enthält diese Website eine MOOC-Beschreibung, eine Kapitelübersicht, ein Impressum und natürlich diesen Bereich.
			<br/>
			Ich werde mir hier erlauben einige Bemerkungen zu den Kapiteln bzw. zum MOOC und den Aufgaben allgemein niederzuschreiben und ebenso sollte das MOOC-Team nicht 
			unerwähnt bleiben.
			<br/><br/>
			Zu erst die Antwort auf die Frage warum ich mir das in meiner spärlichen Freizeit "antue". Aus meiner Sicht recht einfach zu beantworten: "Weil ich es will!".
			<br/>
			Nein, im Ernst! Ich finde das persönliche Weiterbildung, völlig unabhängig vom Bildungsstand...Alter...Beruf, unheimlich wichtig ist.
			Ebenso bin ich mir bewusst das MOOCs noch recht neu in Deutschland sind und sich gerade noch im Aufbau befindet. Ich möchte dabei sein, die Entwicklung
			beobachten und halte diese Form der Weiterbildung (welche zumeist sich tatsächlich auf Hochschul-Niveau befindet) für sehr erfolg versprechend und zukunftsweisend.
			<br/>
			Selbstverständlich muss dieser Neuerung im Zuge der Weiterentwicklung auch Fehler oder Probleme zugestanden werden. Dieser MOOC z.B. befindet sich auf der jungen Plattform
			von <a href="https://iversity.org" onclick="window.open(this.href);return false;">iversity.org</a> und dies dann direkt mit der Plattform von <a href="https://openhpi.de" onclick="window.open(this.href);return false;">OpenHPI</a> zu vergleichen, wäre in sofern etwas "unfair" da iversity mehrere MOOCs gleichzeitig anbietet und
			OpenHPI nur eine. Weiterhin existiert OpenHPI deutlich länger und die Mitarbeiter des Hasso-Plattner-Instituts an der Unversität Potsdam haben somit mehr Erfahrung mit
			der neuen Art der Weiterbildung udn haben hier und da bereits ihre Lehren gelernt.
			<br/><br/>
			Warum aber ausgerechnet den Start mit diesem MOOC? Nun, ich bin seit über 20 Jahren in der IT tätig, habe bereits mehrere Website erstellt (zumeist mit statischem Inhalt)
			und erhoffte mir neue Inputs für die Gestaltung, Umsetzung und Nutzung neuer Technologien. Weiterhin wollte ich meine, sicherlich schon recht veralteten, Kenntnisse über
			CSS, deren sinnvolle Nutzung und HTML5 auf den neuesten Stand bringen. Ausserdem interessiere ich mich für den MOOC 
			<a href="https://iversity.org/courses/web-engineering-ii-developing-mobile-html5-apps" onclick="window.open(this.href);return false;">"Web Engineering II - Developing HTML5 Apps"</a> und daher
